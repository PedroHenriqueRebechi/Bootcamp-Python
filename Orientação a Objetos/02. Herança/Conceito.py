# HERANÇA - É a capacidade de uma classe filha herdar as características da classe pai

# Herança simples - herda de apenas uma classe pai
# Sintaxe
class A: # Classe pai/base
    pass

class B(A): # Classe filha
    pass


# Herança múltipla - herda de várias classes pai
# Sintaxe 
class C(A, B):
    pass